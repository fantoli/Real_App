    private void jButton_Add_PropertyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Add_PropertyActionPerformed
       
        int type = Integer.parseInt((String)jComboBox_Type.getSelectedItem());
        int square_feet = Integer.parseInt(jTextField_SquareFeet.getText());
        int ownerId = Integer.parseInt(jTextField_OwnerId.getText());
        String price = jTextField_Price.getText();
        String address = jTextArea_Address.getText();
        String description = jTextArea_Description.getText();
        int bedrooms = (Integer)jSpinner_Bedrooms.getValue();
        int bathrooms = (Integer)jSpinner_Bathrooms.getValue();
        int age = (Integer)jSpinner_Age.getValue();
        boolean balcony = false;
        boolean pool = false;
        boolean backyard = false;
        boolean garage = false;
        
        //check if the property have a balcony, pool...
        if(jCheckBox_Balcony.isSelected()) balcony = true;
        if(jCheckBox_Pool.isSelected()) pool = true;
        if(jCheckBox_Backyard.isSelected()) backyard = true;
        if(jCheckBox_Garage.isSelected()) garage = true;

        try {
            Connection con = Conexion.getConexion();
            PreparedStatement ps = con.prepareStatement("INSERT INTO property (type, square_feet, ownerId, price, address, bedrooms, bathrooms, age, balcone, pool, backyard, garage, description) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?)");
            ps.setInt(1, type);
            ps.setInt(2, square_feet);
            ps.setInt(3, ownerId);
            ps.setString(4, price);
            ps.setString(5, address);
            ps.setInt(6, bedrooms);
            ps.setInt(7, bathrooms);
            ps.setInt(8, age);
            ps.setBoolean(9, balcony);
            ps.setBoolean(10, pool);
            ps.setBoolean(11, backyard);
            ps.setBoolean(12, garage);
            ps.setString(13, description);
            ps.executeUpdate();
            JOptionPane.showInternalMessageDialog(null, "Property guardada");
            limpiar();
//            cargarTabla();

        } catch (SQLException e) {
            JOptionPane.showInternalMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton_Add_PropertyActionPerformed

